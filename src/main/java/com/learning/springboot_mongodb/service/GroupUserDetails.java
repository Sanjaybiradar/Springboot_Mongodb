package com.learning.springboot_mongodb.service;

import com.learning.springboot_mongodb.entity.Employee;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.stream.Collectors;

public class GroupUserDetails implements UserDetails {

    private String empName;
    private String password;
    private boolean isActive;
    private List<GrantedAuthority> authorities;

    public GroupUserDetails(Employee employee){
        this.empName= employee.getEmpName();
        this.password=employee.getPassword();
        this.isActive= employee.isActive();
        this.authorities= Arrays.stream(employee.getRoles().split(","))
                                    .map(SimpleGrantedAuthority::new).collect(Collectors.toList());
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return empName;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return isActive;
    }
}
